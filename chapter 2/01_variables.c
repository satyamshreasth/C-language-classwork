#include <stdio.h>

int main()
{
    int i = 10; // declare and initialize 'i' with 10
    int j = i;
    int a = 2, b = 3, c = 4, d = 5;
    // %d is called format specifier
    printf("the value of i is %d and value of j is %d\n", i, j);
    printf("the value of a is %d and value of j is %d\n", a, b);
    printf("the value of c is %d and value of j is %d\n", c, d);
   
    return 0;
}
